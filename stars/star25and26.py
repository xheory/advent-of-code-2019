from common.intcode import IntcodeComputer
import subprocess

with open("input/star25") as input_file:
    program = input_file.read().strip()


def run_star25():
    arcade_cabinet = IntcodeComputer(program)
    arcade_cabinet.run()
    block_count = 0
    for i in range(0, len(arcade_cabinet.output_array), 3):
        x, y, tile_id = arcade_cabinet.output_array[i : i + 3]
        block_count += 1 if tile_id == 2 else 0
    return f"Block count: {block_count}"


def load_screen(screen, output_array):
    for i in range(0, len(output_array), 3):
        x, y, tile_id = output_array[i : i + 3]
        screen[x, y] = tile_id
    return screen


#    Empty  0
# █  Wall   1
# X  Block  2
# =  Paddle 3
# o  Ball   4


def get_tile_char(tile_id):
    if tile_id == 0:
        return " "
    elif tile_id == 1:
        return "█"
    elif tile_id == 2:
        return "X"
    elif tile_id == 3:
        return "="
    elif tile_id == 4:
        return "o"


def print_screen(screen):
    subprocess.call("clear", shell=True)
    for y in range(0, 21):
        for x in range(0, 36):
            tile = get_tile_char(screen[x, y])
            print(tile, end="")
        print()


